#include<iostream>
using namespace std;

int main(){
    int x = 5;
    x += 5; // x = x + 5
    cout << x << endl;
    x *= 5; // x = x * 5
    cout << x << endl;
    x -= 5; // x = x - 5
    cout << x << endl;
    x /= 3; // x = x / 3
    cout << x << endl;
    x %= 3; // x = x % 3
    cout << x << endl;
}